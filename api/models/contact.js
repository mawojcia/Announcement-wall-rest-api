const mongoose = require('mongoose');

const messageSchema = mongoose.Schema({
    autor: {
        type: String,
        required: true,
        match: /[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/
    },
    message: {
        type: String,
        required: true,

    }
});

module.exports = mongoose.model('Message', messageSchema);